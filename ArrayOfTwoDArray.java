public class ArrayOfTwoDArray {
    public static void main(String[] args) {
        int x[][][] = {
                {
                        { 10, 20, 99 },
                        { 30, 40, 88 }
                },
                {
                        { 50, 60, 77 },
                        { 80, 70, 66 }
                },
        };
        System.out.println(x[1][1][2]);
    }

}
