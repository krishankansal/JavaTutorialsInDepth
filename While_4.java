public class While_4 {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println();
        int j = 0;
        do {
            j++;
            System.out.print(j + " ");
        } while (j <= 10);

    }

}
